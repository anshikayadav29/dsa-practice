#include <iostream>
using namespace std;

int main() {
    // Integer variable
    int a = 10;

    // Long variable
    long b = 15;

    // Long long variable (very large number)
    long long c = 150000000;

    // Float and double
    float x = 5.6;   // Decimal number
    float y = 5;     // Converted to 5.0 internally

    cout << "Value of y: " << y << endl;

    return 0;
}